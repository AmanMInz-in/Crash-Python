states=["chhattisgarh","madhya pradesh","rajasthan","gujarat","maharashtra"]
print(states[2:4])
# this will print from index 2 to index 3 only index 4 will not be included

#  if you want to print from a paticular index and so on you can do like this
print("\n printing from index 2 to the end of the list")
print(states[2:])
# this will print from index 2 to the end of the list

#  or we can print from biginning to a particular index like this
print("\n printing from the biginning to index 3")
print(states[:3])